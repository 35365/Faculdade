            if(maxflow.value()==0){
                this.jTextAreaFordFulkerson.append("\nnull");
                return;
            }
            for (int v = 0; v < subG.V(); v++) {
                for (FlowEdge e : subG.adj(v)) {
                    if ((v == e.from()) && e.flow() > 0) {
                        this.jTextAreaFordFulkerson.append("\n" + e.toString());
                    }
                }
            }
            this.jTextAreaFordFulkerson.append("\n\nMin cut: ");
            for (int v = 0; v < subG.V(); v++) {
                if (maxflow.inCut(v)) {
                    this.jTextAreaFordFulkerson.append(v + " ");
                }
            }
            this.jTextAreaFordFulkerson.append("\n\nValor máximo de fluxo = " + maxflow.value());
