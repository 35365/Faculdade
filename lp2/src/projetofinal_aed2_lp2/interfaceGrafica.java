/**
 * Faz com que apareça a travesia BellmanFordSP do SubGrafo ou do Grafo nas textAreas, convertendo o grafo e o subGrafo de FlowNetwork para EdgeWeightedDigraph.
 * @param evt 
 */
    private void jButtonResultadoBellmanFordSPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonResultadoBellmanFordSPActionPerformed
        String s[] = this.jComboBoxBellmanFordSP.getSelectedItem().toString().split("-");
        int aux = Integer.parseInt(s[0]);
        this.jTextAreaBellmanFordSP.setText("");
        if(this.jButtonGrafoBellmanFordSP.isVisible()==true){
            EdgeWeightedDigraph gBF = new EdgeWeightedDigraph(g.V());
            for(FlowEdge f : g.edges()){
                gBF.addEdge(new DirectedEdge(f.from(),f.to(),f.capacity()));
            }
            BellmanFordSP sp = new BellmanFordSP(gBF, aux);
            for (int v = 0; v < g.V(); v++) {
                if (sp.hasPathTo(v)) {
                    this.jTextAreaBellmanFordSP.append("\n"+s[0] +" to "+ Integer.toString(v) +"\t"+ Double.toString(sp.distTo(v)));//acrecenta á textArea ao que está lá
                    for (DirectedEdge e : sp.pathTo(v)) {
                        this.jTextAreaBellmanFordSP.append(" "+ e.toString());
                    }
                }
                else {
                    this.jTextAreaBellmanFordSP.append("\n"+s[0]+" to "+ Integer.toString(v)+"\tno path");
                }
            }
        }
        else {
            EdgeWeightedDigraph gBF = new EdgeWeightedDigraph(g.V());
            for(FlowEdge f : subG.edges()){
                gBF.addEdge(new DirectedEdge(f.from(),f.to(),f.capacity()));
                }
            BellmanFordSP sp = new BellmanFordSP(gBF, aux);
            for (int v = 0; v < subG.V(); v++) {
                if (sp.hasPathTo(v)) {
                    this.jTextAreaBellmanFordSP.append("\n"+s[0] +" to "+ Integer.toString(v) +"\t"+ Double.toString(sp.distTo(v)));
                    for (DirectedEdge e : sp.pathTo(v)) {
                        this.jTextAreaBellmanFordSP.append(" "+ e.toString());
                    }
                }
                else {
                    this.jTextAreaBellmanFordSP.append("\n"+s[0]+" to "+ Integer.toString(v)+"\tno path");
                }
            }
        }
    }//GEN-LAST:event_jButtonResultadoBellmanFordSPActionPerformed
