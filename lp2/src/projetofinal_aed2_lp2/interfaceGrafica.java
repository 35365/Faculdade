/**
 * Faz com que apareça a travesia Dijkstra na textArea, convertendo o grafo de FlowNetwork para EdgeWeightedDigraph
 * @param evt 
 */
    private void jButtonResultadoDijkstraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonResultadoDijkstraActionPerformed
        String s[] = this.jComboBoxDOrigem.getSelectedItem().toString().split("-");// Obtem o valor do nó id do nó origem
        int aux = Integer.parseInt(s[0]);
        EdgeWeightedDigraph gDijkstra = new EdgeWeightedDigraph(g.V());
        for(FlowEdge f : g.edges()){
            gDijkstra.addEdge(new DirectedEdge(f.from(),f.to(),f.capacity()));
        }
        DijkstraSP dijMST = new DijkstraSP(gDijkstra,aux);
        s = this.jComboBoxDDestino.getSelectedItem().toString().split("-");// Obtem o valor do nó id do nó de destino
        aux = Integer.parseInt(s[0]);
        if(dijMST.pathTo(aux)==null){
            this.jTextAreaDijkstra.setText("null");
            return;
        }
        for( DirectedEdge e : dijMST.pathTo(aux))
            this.jTextAreaDijkstra.setText(e.toString());
    }//GEN-LAST:event_jButtonResultadoDijkstraActionPerformed

