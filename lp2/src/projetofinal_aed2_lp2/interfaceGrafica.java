/**
 * Insere os nós selecionados na ComboBox no subGrafo auxiliar
 * @param evt 
 */
    private void jButtonSubGrafoInserirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSubGrafoInserirActionPerformed
        String s[] = this.jComboBoxSubGrafo.getSelectedItem().toString().split("-");// id do nó 
        int aux = Integer.parseInt(s[0]);
        for(NoMoradia gi : g.getNodesM()){
            if(gi.getId()== aux){// procura o nó na lista de nos 
                subGAux.addNode(gi);// adiciona o nó ao grafo auxiliar
                this.jComboBoxSubGrafo.removeItemAt(this.jComboBoxSubGrafo.getSelectedIndex());// remove do ComboBox pois não se pode adicionar outravez o mesmo
            }   
        }
        for(NoFonteEnergia gi : g.getNodesF()){
            if(gi.getId()== aux){
                subGAux.addNode(gi);
                this.jComboBoxSubGrafo.removeItemAt(this.jComboBoxSubGrafo.getSelectedIndex());
            } 
        }

    }//GEN-LAST:event_jButtonSubGrafoInserirActionPerformed
/**
 * Faz as ligações entre os nós que pertencem ao subGrafo auxiliar.
 * Nesta ação é quando o subGrafo é finalizado, isto é, caso adicionem nós e não finalizem não altera nas outras tabs.
 * @param evt 
 */
    private void jButtonSubGrafoFinalizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSubGrafoFinalizarActionPerformed
        for(FlowEdge di: this.g.edges())// Procura as arestas que pertencem ao subgrafo, isto é se o nó de origem e detino pertencem ao subgrafo
            if(this.subGAux.pertenceGrafo(di.from())==true && this.subGAux.pertenceGrafo(di.to())==true){
                this.subGAux.addEdge(di);
            } 
        subG = subGAux;// utilizei um grafo auxiliar pois assim caso adicionem nós e não finalizem não altera nas outras tabs
        this.jTextAreaSubGrafo.setText(this.subG.toString());
        this.iniciarJComboBox();
    }//GEN-LAST:event_jButtonSubGrafoFinalizarActionPerformed
/**
 * Faz com que apareça a travesia DFS do grafo nas textAreas, convertendo o grafo de FlowNetwork para EdgeWeightedDigraph.
 * @param evt 
 */
    private void jButtonGrafoDFSActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGrafoDFSActionPerformed
        EdgeWeightedDigraph gDFS = new EdgeWeightedDigraph(g.V());
        for(FlowEdge f : g.edges()){
            gDFS.addEdge(new DirectedEdge(f.from(),f.to(),f.capacity()));
        }
        DepthFirstOrder dfs = new DepthFirstOrder(gDFS);
        this.jTextAreaPreOrdem.setText(dfs.pre().toString());
        this.jTextAreaPosOrdem.setText(dfs.post().toString());
        this.jTextAreaPosOrdemInv.setText(dfs.reversePost().toString());
    }//GEN-LAST:event_jButtonGrafoDFSActionPerformed
