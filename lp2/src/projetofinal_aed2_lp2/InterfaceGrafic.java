/**
 * Remove do grafo o nó selecionado na ComboBox.
 * @param evt 
 */
    private void jButtonRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRemoverActionPerformed
        String s[] = this.jComboBoxRemover.getSelectedItem().toString().split("-");
        int aux = Integer.parseInt(s[0]);
        this.jComboBoxRemover.removeItemAt(this.jComboBoxRemover.getSelectedIndex());
        GrafoDirigido subG = new GrafoDirigido(grafo.V());
        for(NoMoradia gi : grafo.getNodesM()){
            if(gi.getId()!= aux){
                subG.addNode(gi);
            }
        }
        for(NoFonteEnergia gi : grafo.getNodesF()){
            if(gi.getId()!= aux){
                subG.addNode(gi);
            } 
        }
        for(FlowEdge di: this.grafo.edges())
            if(subG.pertenceGrafo(di.from())==true && subG.pertenceGrafo(di.to())==true){
                subG.addEdge(di);
            } 
        this.grafo = subG;
        this.jTextAreaRemover.setText(subG.toString());
    }//GEN-LAST:event_jButtonRemoverActionPerformed
