/**
 * Função para ler o grafo guardado em ficheiro binário.
 * @throws FileNotFoundException
 * @throws IOException
 * @throws ClassNotFoundException 
 */
    public void lerGrafoBin() throws FileNotFoundException, IOException, ClassNotFoundException{//  Lê o grafo do ficheiro binario para ser utilizado
        String filename = ".//data//GrafoDirigidoBin.bin";
        File f = new File(filename);
        FileInputStream fis = new FileInputStream(f);
        ObjectInputStream ois = new ObjectInputStream(fis);
        this.grafo = (GrafoDirigido)ois.readObject();
    }
