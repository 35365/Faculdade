/**
 * Restaura os items das ComboBox do painel grafo com o do grafo que foi carregado.
 * @param evt 
 */
    private void jTabbedPaneLerEscreverStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_jTabbedPaneLerEscreverStateChanged
        this.jComboBoxVDestino.removeAllItems();
        this.jComboBoxVOrigem.removeAllItems();
        this.jComboBoxMoradias.removeAllItems();
        this.jComboBoxFontes.removeAllItems();
        for(Moradia m : this.moradias){
            this.jComboBoxMoradias.addItem(m.hashCode()+"-"+m.toString());
        }
        for(FonteEnergiaG f : this.fontes){
            this.jComboBoxFontes.addItem(f.hashCode()+"-"+f.toString());
        }
    }//GEN-LAST:event_jTabbedPaneLerEscreverStateChanged

